"""
Benchmark real de transcrição: mede WER e preservação de termos técnicos
(code-switching PT-EN) executando os provedores de verdade sobre os áudios
sintéticos.

Este script **não simula** resultados. Se um provedor não estiver disponível
(dependência ausente, chave não configurada), ele é reportado como pulado —
nunca substituído por dados fabricados.

Uso:
    python scripts/run_benchmark.py                       # todos disponíveis
    python scripts/run_benchmark.py --providers deepgram local
    python scripts/run_benchmark.py --audio-dir data/synthetic
"""

import argparse
import importlib.util
import json
import os
import time
import wave
from pathlib import Path
from typing import Any

import jiwer

from app.text_utils import clean_text  # noqa: F401  (re-exported for tests/tooling)

TECH_TERMS = [
    "pytest", "docker", "postgres", "postgresql", "redis", "fastapi", "github actions",
    "ci/cd", "deploy", "merge", "pull request", "caching", "code review", "solid", "clean code",
    "backend", "workflow", "code smell", "refactoring"
]

ALL_PROVIDERS = ("deepgram", "local", "openai")

PROVIDER_LABELS = {
    "deepgram": "Deepgram nova-3 (API)",
    "local": "WhisperX (local)",
    "openai": "OpenAI whisper-1 (API)",
}


def calculate_wer_jiwer(reference: str, hypothesis: str) -> float:
    """Calculates Word Error Rate (WER) using the jiwer library."""
    ref_clean = clean_text(reference)
    hyp_clean = clean_text(hypothesis)
    if not ref_clean:
        return 1.0 if hyp_clean else 0.0
    return jiwer.wer(ref_clean, hyp_clean)


def analyze_tech_terms(reference: str, hypothesis: str) -> tuple[list[str], list[str]]:
    """
    Checks which technical terms were successfully transcribed.
    Returns (correct_terms, missed_terms).
    """
    ref_clean = clean_text(reference)
    hyp_clean = clean_text(hypothesis)

    correct = []
    missed = []

    for term in TECH_TERMS:
        term_clean = clean_text(term)
        if term_clean in ref_clean:
            if term_clean in hyp_clean:
                correct.append(term)
            else:
                missed.append(term)

    return correct, missed


def provider_unavailable_reason(provider: str) -> str | None:
    """Returns why a provider cannot run, or None when it is ready."""
    if provider == "local":
        if importlib.util.find_spec("whisperx") is None:
            return "whisperx não instalado (pip install -r requirements-ml.txt)"
        return None
    if provider == "openai":
        if not os.getenv("OPENAI_API_KEY"):
            return "OPENAI_API_KEY não definida"
        return None
    if provider == "deepgram":
        if not os.getenv("DEEPGRAM_API_KEY"):
            return "DEEPGRAM_API_KEY não definida"
        return None
    return f"provedor desconhecido: {provider}"


def build_engine(provider: str) -> Any:
    from app.audio_processor import (
        DeepgramTranscription,
        LocalTranscription,
        OpenAITranscription,
    )

    if provider == "deepgram":
        return DeepgramTranscription()
    if provider == "openai":
        return OpenAITranscription()
    return LocalTranscription()


def audio_duration_seconds(audio_path: Path) -> float | None:
    try:
        with wave.open(str(audio_path)) as w:
            return w.getnframes() / w.getframerate()
    except Exception:
        return None


def warn_if_audio_looks_stale(audio_path: Path, reference_text: str) -> str | None:
    """
    The synthetic .wav files are gitignored and regenerated by
    scripts/generate_synthetic.py from the same dialogue that produces the
    reference JSON. A local .wav left over from an older dialogue silently
    invalidates the WER, so flag an implausible duration/length ratio.
    """
    duration = audio_duration_seconds(audio_path)
    if duration is None:
        return None
    words = len(clean_text(reference_text).split())
    if words == 0:
        return None
    # Natural speech sits around 2-4 words/second; TTS is close to that.
    words_per_second = words / duration
    if words_per_second > 6:
        return (
            f"o áudio tem {duration:.0f}s para {words} palavras de referência "
            f"({words_per_second:.1f} palavras/s). O .wav provavelmente está "
            f"desatualizado — regenere com scripts/generate_synthetic.py."
        )
    return None


def transcribe_with(provider: str, audio_path: Path) -> tuple[str, float]:
    """Runs a provider and returns (hypothesis_text, elapsed_seconds)."""
    engine = build_engine(provider)
    t0 = time.time()
    segments = engine.transcribe(audio_path)
    elapsed = time.time() - t0
    return " ".join(seg["text"] for seg in segments), elapsed


def run_benchmark(audio_dir: Path, output_report_path: Path, providers: list[str]):
    print("Starting WER benchmark (real transcription, no simulation)...")

    audio_files = sorted(audio_dir.glob("*.wav"))
    if not audio_files:
        print(f"Erro: nenhum áudio (*.wav) encontrado em '{audio_dir}'.")
        print("Gere o dataset com: python scripts/generate_synthetic.py")
        return 1

    skipped = {}
    active = []
    for provider in providers:
        reason = provider_unavailable_reason(provider)
        if reason:
            skipped[provider] = reason
            print(f"PULANDO {provider}: {reason}")
        else:
            active.append(provider)

    if not active:
        print("\nNenhum provedor disponível. Configure ao menos um e tente de novo.")
        return 1

    print(f"Provedores ativos: {', '.join(active)}")
    print(f"Áudios encontrados: {len(audio_files)}")

    results = []
    warnings: list[str] = []

    for audio_path in audio_files:
        prefix = audio_path.stem.replace("interview_", "")
        json_path = audio_dir / f"interview_{prefix}.json"

        if not json_path.exists():
            print(f"Aviso: metadados JSON ausentes para '{audio_path.name}'. Pulando.")
            continue

        with open(json_path, encoding="utf-8") as f:
            dialogue_data = json.load(f)

        turns = dialogue_data.get("turns", [])
        reference_text = " ".join(t["text"] for t in turns)

        stale = warn_if_audio_looks_stale(audio_path, reference_text)
        if stale:
            warnings.append(f"`{audio_path.name}`: {stale}")
            print(f"AVISO: {audio_path.name}: {stale}")

        entry: dict[str, Any] = {"filename": audio_path.name, "providers": {}}

        for provider in active:
            try:
                hypothesis, elapsed = transcribe_with(provider, audio_path)
            except Exception as e:
                print(f"  {provider}: FALHOU ({type(e).__name__}: {e})")
                entry["providers"][provider] = {"error": f"{type(e).__name__}: {e}"}
                continue

            wer = calculate_wer_jiwer(reference_text, hypothesis)
            correct, missed = analyze_tech_terms(reference_text, hypothesis)
            entry["providers"][provider] = {
                "wer": wer,
                "time": elapsed,
                "correct": correct,
                "missed": missed,
            }
            print(f"  {provider}: WER={wer:.2%} em {elapsed:.1f}s")

        results.append(entry)

    if not results:
        print("Nenhuma amostra processada.")
        return 1

    _write_report(output_report_path, results, active, skipped, warnings)
    print(f"\nRelatório salvo em: {output_report_path}")
    return 0


def _averages(results: list[dict], provider: str) -> tuple[float | None, float | None]:
    values = [
        r["providers"][provider]
        for r in results
        if provider in r["providers"] and "wer" in r["providers"][provider]
    ]
    if not values:
        return None, None
    return (
        sum(v["wer"] for v in values) / len(values),
        sum(v["time"] for v in values) / len(values),
    )


def _write_report(
    output_report_path: Path,
    results: list[dict],
    active: list[str],
    skipped: dict[str, str],
    warnings: list[str],
):
    os.makedirs(output_report_path.parent, exist_ok=True)

    lines = [
        "# Relatório de Benchmark WER (Word Error Rate)",
        "",
        f"**Data da execução:** {time.strftime('%Y-%m-%d %H:%M:%S')}",
        "",
        "**Objetivo:** comparar a taxa de erro de transcrição entre os provedores "
        "suportados, com atenção a termos técnicos em inglês mesclados ao "
        "português (code-switching).",
        "",
        "Todos os números abaixo vêm de **execuções reais** dos provedores sobre "
        "os áudios sintéticos. Provedores indisponíveis são listados como "
        "pulados — este relatório nunca contém dados simulados.",
        "",
    ]

    if warnings:
        lines.extend([
            "> ⚠️ **Avisos de integridade do dataset**",
            ">",
            *[f"> - {w}" for w in warnings],
            "",
        ])

    if skipped:
        lines.extend(["## Provedores pulados", ""])
        lines.extend(f"- **{PROVIDER_LABELS.get(p, p)}**: {r}" for p, r in skipped.items())
        lines.append("")

    lines.extend([
        "## 1. Média geral",
        "",
        "| Provedor | WER médio | Tempo médio |",
        "| :--- | :---: | :---: |",
    ])
    for provider in active:
        avg_wer, avg_time = _averages(results, provider)
        if avg_wer is None:
            lines.append(f"| **{PROVIDER_LABELS.get(provider, provider)}** | — | — |")
        else:
            lines.append(
                f"| **{PROVIDER_LABELS.get(provider, provider)}** "
                f"| {avg_wer:.2%} | {avg_time:.1f}s |"
            )

    lines.extend(["", "## 2. Resultados por amostra", ""])
    header = "| Arquivo | " + " | ".join(PROVIDER_LABELS.get(p, p) for p in active) + " |"
    sep = "| :--- | " + " | ".join([":---:"] * len(active)) + " |"
    lines.extend([header, sep])

    for r in results:
        cells = []
        for provider in active:
            data = r["providers"].get(provider)
            if not data:
                cells.append("—")
            elif "error" in data:
                cells.append("erro")
            else:
                cells.append(f"{data['wer']:.2%} ({data['time']:.1f}s)")
        lines.append(f"| `{r['filename']}` | " + " | ".join(cells) + " |")

    lines.extend([
        "",
        "## 3. Code-switching e termos técnicos",
        "",
        "Termos técnicos em inglês presentes na referência e se cada provedor os "
        "preservou na transcrição.",
        "",
    ])

    for r in results:
        lines.extend([f"### `{r['filename']}`", ""])
        for provider in active:
            data = r["providers"].get(provider)
            lines.append(f"#### {PROVIDER_LABELS.get(provider, provider)}")
            if not data:
                lines.extend(["- Não executado.", ""])
                continue
            if "error" in data:
                lines.extend([f"- **Erro:** {data['error']}", ""])
                continue
            correct = ", ".join(f"`{t}`" for t in data["correct"]) or "Nenhum"
            missed = ", ".join(f"`{t}`" for t in data["missed"]) or "Nenhum"
            lines.extend([
                f"- **Preservados:** {correct}",
                f"- **Perdidos ou incorretos:** {missed}",
                "",
            ])

    with open(output_report_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Benchmark real de WER entre os provedores de transcrição."
    )
    parser.add_argument("--audio-dir", default="data/synthetic",
                        help="Diretório com os áudios sintéticos")
    parser.add_argument("--output", default="docs/reports/benchmark_wer_report.md",
                        help="Caminho do relatório markdown gerado")
    parser.add_argument("--providers", nargs="+", default=list(ALL_PROVIDERS),
                        choices=list(ALL_PROVIDERS),
                        help="Provedores a comparar (padrão: todos os disponíveis)")

    args = parser.parse_args()

    raise SystemExit(
        run_benchmark(Path(args.audio_dir), Path(args.output), args.providers)
    )
